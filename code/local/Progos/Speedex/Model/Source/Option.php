    public function timeArray()
    {        
        $result = array();
        for ($i = 0; $i < 24; $i++) {
            $hour = $i;
            if ($hour < 10) {
                $hour = '0'.$hour;
            }
            $result[] = array(
                'label' => $hour.':00',
                'value' => $i * 60,
            );
            $result[] = array(
                'label' => $hour.':30',
                'value' => $i * 60 + 30,
            );
        }
        return $result;
    }
